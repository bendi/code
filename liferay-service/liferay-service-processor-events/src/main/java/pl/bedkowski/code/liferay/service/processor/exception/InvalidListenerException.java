package pl.bedkowski.code.liferay.service.processor.exception;

public class InvalidListenerException extends Exception {

	private static final long serialVersionUID = 3626806819652421065L;

	public InvalidListenerException() {
		super();
	}

	public InvalidListenerException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public InvalidListenerException(String arg0) {
		super(arg0);
	}

	public InvalidListenerException(Throwable arg0) {
		super(arg0);
	}

}
